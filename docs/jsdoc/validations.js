/**
 * Validation helpers used by the contact form.
 *
 * @module validations
 */

/**
 * Checks whether a name contains at least two valid characters.
 *
 * @function isValidName
 * @param {string} value - Name to validate.
 * @returns {boolean} True when the name is valid.
 */

/**
 * Checks whether an email address has a valid basic format.
 *
 * @function isValidEmail
 * @param {string} value - Email address to validate.
 * @returns {boolean} True when the email is valid.
 */

/**
 * Checks whether a birth date belongs to an adult.
 *
 * @function isAdult
 * @param {string} birthDate - Birth date in YYYY-MM-DD format.
 * @returns {boolean} True when the person is at least 18 years old.
 */

/**
 * Checks whether a postal code is a valid French postal code.
 *
 * @function isValidFrenchPostalCode
 * @param {string} postalCode - Postal code to validate.
 * @returns {boolean} True when the postal code has five digits.
 */
